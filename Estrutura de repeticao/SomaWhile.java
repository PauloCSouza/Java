class SomaWhile {

// na estrutura de repetição while

	public static void main (String args [ ])
	{
		int n; // criar variável n
		n = 0; // instanciar a variável n com zero
		
		while (n < 100) // enquanto (n<100)
		{
			
			n = n + 1; // acrescentando 1 ao valor de n
			System.out.println ("numero " + n); // exibir valor n
			
		} // fim do while
		System.exit(0);
	} // fim do void main
} // fim da classe
    
    
